    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Registro doc = new Registro();
       String nombreU = nombreUsuario.getText();
       String contra = contrasena.getText();
       String grup= grupo.getText();
       String nom = nombre.getText();
       String pro= programa.getText();
       
        if (!nombreU.equals("")&&!contra.equals("")&&!grup.equals("")&&!nom.equals("")) {
           doc.setNombreUsuarioAgregar(nombreU);
           doc.setContrasenaAgregar(contra);
           doc.setGrupoAgregar(grup);
           doc.setNombreAgregar(nom);
           doc.setProgramaAgregar(pro);
           
           
           try {
               doc.agregarEstudiante();
           } catch (SQLException ex) {
               Logger.getLogger(AgregarEstudiante.class.getName()).log(Level.SEVERE, null, ex);
           }
           nombreUsuario.setText("");
           contrasena.setText("");
           grupo.setText("");
           nombre.setText("");
           programa.setText("");
            JOptionPane.showMessageDialog(null,"Registro exitoso");
        }else{
          JOptionPane.showMessageDialog(null,"Llene todos los campos");  
        }
    }//GEN-LAST:event_jButton1ActionPerformed

