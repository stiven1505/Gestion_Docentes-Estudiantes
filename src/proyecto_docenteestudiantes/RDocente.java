    private void registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrarActionPerformed
        Registro registro = new Registro();
        String nombreR = nombre.getText();
        String materiaR = materia.getText();
        String nombre_usuarioR = nombre_usuario.getText().trim();
        String contrasenaR = contrasena.getText();
        if(!nombreR.equals("")&&!materiaR.equals("")&&!nombre_usuarioR.equals("")&&!contrasenaR.equals("")){
        registro.setNombre(nombreR);
        registro.setMateria(materiaR);
        registro.setNombre_usuario(nombre_usuarioR);
        registro.setContrasena(contrasenaR);
        
            try {
                registro.registrarDocentes();
            } catch (SQLException ex) {
                Logger.getLogger(RDocente.class.getName()).log(Level.SEVERE, null, ex);
            }
        nombre.setText("");
        materia.setText("");
        nombre_usuario.setText("");
        contrasena.setText("");
        
        JOptionPane.showMessageDialog(null,"Registro exitoso");
        }
        else{
          JOptionPane.showMessageDialog(null,"Llene todos los campos");  
        }
    }//GEN-LAST:event_registrarActionPerformed

