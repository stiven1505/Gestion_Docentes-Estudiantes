    private void grupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_grupActionPerformed
        
        Conexionbd cc = new Conexionbd();
        Connection con;
       Grupo gr = new Grupo();
       grupo= gr.seleccion();
      

            DefaultTableModel dfm = new DefaultTableModel();
            tabla = this.Tabla;
            tabla.setModel(dfm);

            dfm.setColumnIdentifiers(new Object[]{"Nombre usuario ", "Nombre", "Grupo", "Programa"});
            try {
                con = cc.conexion();
                Statement stmt = con.createStatement();

                ResultSet rs = stmt.executeQuery("SELECT * FROM estudiante WHERE grupo ='"+grupo+"'");

                while (rs.next()) {
                    dfm.addRow(new Object[]{rs.getString("nombre_usuario"), rs.getString("nombre"), rs.getString("grupo"), rs.getString("programa")});
                }
            } catch (SQLException ex) {
                Logger.getLogger(BucarNotas.class.getName()).log(Level.SEVERE, null, ex);
            }
        
        
    }//GEN-LAST:event_grupActionPerformed

