    private void presenteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_presenteActionPerformed
        Docente doc =new Docente();
        Asistencia asistencia = new Asistencia();
        String nom= nombreUsuario.getText();
        
        if(!nom.equals("")){
            doc.setAsistenciaNombreUsuario(nom);
            doc.setAsistenciaGrupo(grupo);
            doc.setAsistenciaDocente(x);
            try {
                doc.asistencia(asistencia);
            } catch (SQLException ex) {
                Logger.getLogger(RegistroListadoAsistencia.class.getName()).log(Level.SEVERE, null, ex);
            }
                nombreUsuario.setText("");
        }else{
            JOptionPane.showMessageDialog(null,"Seleccione un campo");
        }
    
        
        
        


        
    }//GEN-LAST:event_presenteActionPerformed

