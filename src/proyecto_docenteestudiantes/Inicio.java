    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        Usuario usuarioo = new Usuario();
        Docente doc =new Docente();
        Estudiante estudiantes =new Estudiante();
        
        String usuarioR = usuario.getText();
        String contrasenaR = contrasena.getText();
        String rolR = "";
        
     
        if (docente.isSelected()) {
            rolR = "docente";
        }
        if (estudiante.isSelected()) {
            rolR = "estudiante";
        }
        if(admin.isSelected()){
            rolR="admin";
        }
        if (!usuarioR.equals("") && !contrasenaR.equals("") && !rolR.equals("")) {
            usuarioo.setNombre_usuario(usuarioR);
            
            
            usuarioo.setContrasena(contrasenaR);
            
            
            estudiantes.setNombreUsuario(usuarioR);
            usuarioo.setRol(rolR);
           
            

            try {

              
                usuarioo.validacion();
                this.setVisible(false);

            } catch (SQLException ex) {
                Logger.getLogger(Inicio.class.getName()).log(Level.SEVERE, null, ex);
            }

            usuario.setText("");
            contrasena.setText("");

        } else {
            JOptionPane.showMessageDialog(null, "Llene todos los campos");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

