    private void actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actualizarActionPerformed
        Conexionbd cc = new Conexionbd();
        Connection con;

        DefaultTableModel dfm = new DefaultTableModel();
        tabla = this.Tabla;
        tabla.setModel(dfm);

        dfm.setColumnIdentifiers(new Object[]{"Materia", "Primer parcial", "Segundo Parcial", "Examen final", "Proyecto integrador", "Notas varias", "Promedio"});
        try {
            con = cc.conexion();
            Statement stmt = con.createStatement();

            ResultSet rs = stmt.executeQuery("SELECT * FROM notas WHERE nombre_usuario_estudiante='" + nombre + "'");

            while (rs.next()) {
                
                dfm.addRow(new Object[]{rs.getString("materia"), rs.getDouble("primer_parcial"), rs.getDouble("segundo_parcial"), rs.getDouble("examen_final"), rs.getDouble("proyecto_integrador"), rs.getDouble("notas_varias"), rs.getDouble("promedio")});
            }
        } catch (SQLException ex) {
            Logger.getLogger(BucarNotas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_actualizarActionPerformed

